// Extension for easy gradient usage
extension GradientExtension on LinearGradient {
  BoxDecoration toBoxDecoration({double borderRadius = 20}) {
    return BoxDecoration(
      gradient: this,
      borderRadius: BorderRadius.circular(borderRadius),
    );
  }
}
